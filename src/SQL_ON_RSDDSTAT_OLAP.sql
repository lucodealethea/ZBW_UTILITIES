      RTRIM (
        RTRIM(
          LEFT(
            RTRIM (
              "RVPOLAPSTATM"."TIME"
            ),
            4
          )
        )
      ),
      N'00'
    )
  ) AS "CALMINUTE",
  (
    CONCAT(
      RTRIM (
        RTRIM(
          LEFT(
            RTRIM (
              "RVPOLAPSTATM"."TIME"
            ),
            2
          )
        )
      ),
      N'0000'
    )
  ) AS "CALHOUR",
  "RVPOLAPSTATM"."CALDAY" AS "CALDAY",
  (
    RTRIM(
      LEFT(
        RTRIM (
          "RVPOLAPSTATM"."CALDAY"
        ),
        6
      )
    )
  ) AS "CALMONTH",
  (
    RTRIM(
      LEFT(
        RTRIM (
          "RVPOLAPSTATM"."CALDAY"
        ),
        4
      )
    )
