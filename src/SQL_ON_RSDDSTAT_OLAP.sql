      CASE WHEN NOT (
        "RVPOLAPSTATF"."EVENTID" = N'000009000'
      ) THEN "RVPOLAPSTATF"."EVENTTIME" ELSE CAST(
        0 AS DECIMAL(000021,000000)
      )
      END
    ) WHEN N'MDX' THEN (
      CASE WHEN NOT (
        "RVPOLAPSTATF"."EVENTID" = N'000009000'
      ) THEN "RVPOLAPSTATF"."EVENTTIME" ELSE CAST(
        0 AS DECIMAL(000021,000000)
      )
      END
