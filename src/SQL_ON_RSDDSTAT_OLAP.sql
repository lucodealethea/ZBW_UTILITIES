    "RVPOLAPSTATM"."TIMEFRONTEND"
  ) AS "TIMEFRONTEND",
  SUM(
    "RVPOLAPSTATM"."TIMEOLAP"
  ) AS "TIMEOLAP",
  SUM(
    "RVPOLAPSTATM"."TIMEDM"
